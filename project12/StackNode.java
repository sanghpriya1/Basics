package project12;

public class StackNode {
	private int val;
	private int index;
	
	public StackNode(int val, int index) {
		
		this.val = val;
		this.index = index;
	}

	public int getVal() {
		return val;
	}

	public void setVal(int val) {
		this.val = val;
	}

	public int getIndex() {
		return index;
	}

	public void setIndex(int index) {
		this.index = index;
	}
	
	
	


}
